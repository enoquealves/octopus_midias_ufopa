function update_leg01 () {
    if (leg01_pos == 0 && leg01_dir == -1) {
        leg01_dir = 1
        if (Math.percentChance(80)) {
            leg01_start = 0
        } else {
            leg01_start = 1
        }
    }
    if (leg01_pos == 0) {
        if (leg01_start == 0) {
            leg01_pos = 1
        } else {
            leg01_start += -1
        }
    } else if (leg01_pos == 3) {
        leg01_dir = -1
        leg01_pos += leg01_dir
    } else {
        leg01_pos += leg01_dir
    }
    leg01.setImage(leg01_sprites[leg01_pos])
}
function boatKickOut () {
    if (kick_count < 30) {
        kick_count += 1
    } else {
        kick_count = 0
        frogman_pos = 1
        updateFrogMan()
    }
}
